package praktikum.sesi5;

public class DeretAngka {
    public static void main(String[] args) {
        // Menggunakan perulangan for untuk menampilkan angka dari 1 hingga 5
        for (int i = 1; i <= 5; i++) {
            // Menampilkan angka
            System.out.println("Iterasi " + i + ": Angka = " + i);
        }
    }
}
